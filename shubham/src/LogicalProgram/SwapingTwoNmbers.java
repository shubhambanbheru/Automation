package LogicalProgram;

	public class SwapingTwoNmbers {

		public static void main(String[] args) {

			int a=20, b=30;
			
			System.out.println("Before Swapping values :"+a+" "+b);
			
			int c = a ;  //20 =20
					
			a=b;        //30 =30
			
			b=c;       //20=20
			
			System.out.println("After Swapping values :"+a+" "+b);

			
		}

	}

